# This is a small python program that has a for loop takes a range of numbers 0
# 10 and parses through them by 1 the numbers are passed to x  and are squared 
# then 2 is added and then print the value after the operation. 
# An example invocation is python3 computeFunction.py

for i in range(10):
  x = (i ** 2) + 2
  print(x)




